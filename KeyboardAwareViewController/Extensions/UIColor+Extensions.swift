import Foundation
import UIKit

extension UIColor {
    static var pastelRed: UIColor {
        return UIColor(red: 255/255, green: 179/255, blue: 186/255, alpha: 1)
    }
    
    static var pastelOrange: UIColor {
        return UIColor(red: 255/255, green: 223/255, blue: 186/255, alpha: 1)
    }
    
    static var pastelYellow: UIColor {
        return UIColor(red: 255/255, green: 255/255, blue: 186/255, alpha: 1)
    }
    
    static var pastelGreen: UIColor {
        return UIColor(red: 186/255, green: 255/255, blue: 201/255, alpha: 1)
    }
    
    static var pastelBlue: UIColor {
        return UIColor(red: 186/255, green: 225/255, blue: 255/255, alpha: 1)
    }
}
